# arithmetic operators,assignment operators,logical operator,comparison operator

# Arithmetic Operator
num = 43  # first operand
num2 = 12  # second operand
print(f"The sum of {num}and {num2}is {num + num2}")
print(f"The difference of {num}and {num2}is {num - num2}")
print(f"The product of {num}and {num2}is {num * num2}")
print(f"The remainder of {num}and {num2}is {num % num2}")
print(f"The exponent of {num}and {num2}is {num ** num2}")
print(f"The floor of {num}and {num2}is {num // num2}")

num3 = 20
num4 = 30

# Comparison Operators
print(f"{num3} and {num4} are equals {num3 == num4}")
print(f"{num3} and {num4} are not equal {num3 != num4}")
print(f"{num3} is greater than {num4} {num3 > num4}")
print(f"{num3} is less than {num4} {num3 < num4}")
print(f"{num3} is less or equals to than {num4} {num3 <= num4}")
print(f"{num3} is greater or equals to than {num4} {num3 >= num4}")

num5 = 34
num6 = 45

# Assignment operator
print(f"num5 = num6: {num5 == num6}")
print(f"num5 += num6: {num5 + num6}")
print(f"num5 *= num6: {num5 * num6}")
print(f"num5 -= num6: {num5 - num6}")

